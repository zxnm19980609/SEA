<script type="text/javascript">
    jQuery(function($) {

        /* initialize the external events
            -----------------------------------------------------------------*/

        $('#external-events div.external-event').each(function() {

            // create an Event Object (http://arshaw.com/fullcalendar/docs/event_data/Event_Object/)
            // it doesn't need to have a start or end
            var eventObject = {
                title: $.trim($(this).text()) // use the element's text as the event title
            };

            // store the Event Object in the DOM element so we can get to it later
            $(this).data('eventObject', eventObject);

            // make the event draggable using jQuery UI
            $(this).draggable({
                zIndex: 999,
                revert: true,      // will cause the event to go back to its
                revertDuration: 0  //  original position after the drag
            });

        });




        /* initialize the calendar
        -----------------------------------------------------------------*/

        var date = new Date();
        var d = date.getDate();
        var m = date.getMonth();
        var y = date.getFullYear();

        var course_list = [];
        $.ajax({
            type: 'get',
            url: 'getcourseinfo.php',
            async: false,
            success(data) {
                course_list = JSON.parse(data);
            },
            error(err) {
                console.log(err);
            },
        });

        var events = [];
        for (let course of course_list) {
            course.ctime = JSON.parse(course.ctime);
            for (let lesson of course.ctime) {
                let start_time = new Date();
                start_time.setTime(lesson[0]);
                let end_time = new Date();
                end_time.setTime(lesson[1]);
                events.push({
                    title: course.cname + ' - ' + course.clocat,
                    allDay: false,
                    start: start_time,
                    end: end_time,
                    className: 'label-important',
                    url: 'home.php?cid=' + course.cid,
                });
            }
        }
        console.log(events);

        var calendar = $('#calendar').fullCalendar({
            //isRTL: true,
            //firstDay: 1,// >> change first day of week

            buttonHtml: {
                prev: '<i class="ace-icon fa fa-chevron-left"></i>',
                next: '<i class="ace-icon fa fa-chevron-right"></i>'
            },

            header: {
                left: 'prev,next today',
                center: 'title',
                right: 'month,agendaWeek,agendaDay'
            },

            events,

            allDaySlot: false,
            minTime: "06:00:00",
            maxTime: "22:00:00",

            height : window.innerHeight - 230,
            windowResize: function(view) {
                $('#calendar').fullCalendar('option', 'height', window.innerHeight - 230);
            }

        });
    });
</script>
